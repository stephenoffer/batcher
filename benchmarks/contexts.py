"""Data-context builders shared by every benchmark in a dataset family.

A context prepares one synthetic (or TPC-H) dataset once and exposes the per-
engine handles a case needs: the registered DuckDB connection, the Polars frames,
and the Batcher datasets. Building the data once and handing the same handles to
every case keeps the cases themselves tiny (just the query, three ways).

Two datasets exist today:

- ``synthetic`` (:class:`SyntheticContext`): a fact table and a small dim table,
  the substrate for the operator-mix families (filter, aggregate, join, ...).
- ``tpch`` (:class:`TpchContext`): the eight TPC-H tables, generated by DuckDB's
  built-in ``dbgen``.

Add a dataset by adding a context class with a ``build`` classmethod and teaching
``run.py`` how to size it.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any

import duckdb
import numpy as np
import polars as pl
import pyarrow as pa
import pyarrow.compute as pc

import batcher as bt

# --------------------------------------------------------------------------- #
# Synthetic dataset
# --------------------------------------------------------------------------- #
DEFAULT_ROWS = 10_000_000
N_DIM = 1000  # dim table rows / join-key cardinality
N_KEYS = 1000  # group-by key cardinality (k1)
N_KEYS2 = 50  # second group-by key cardinality (k2)
CATEGORIES = ["alpha", "beta", "gamma", "delta", "epsilon", "zeta", "eta", "theta"]


def make_synthetic_data(n_rows: int, seed: int = 1234) -> tuple[pa.Table, pa.Table]:
    """Build a fact table (~n_rows) and a small dim table as pyarrow Tables."""
    rng = np.random.default_rng(seed)

    cat_idx = rng.integers(0, len(CATEGORIES), size=n_rows)
    cats = np.array(CATEGORIES, dtype=object)[cat_idx]
    # A timestamp column spanning three calendar years so date-part extraction
    # has a few distinct groups.
    days = rng.integers(0, 365 * 3, size=n_rows)
    ts = (np.datetime64("2021-01-01") + days.astype("timedelta64[D]")).astype("datetime64[us]")

    fact = pa.table(
        {
            "id": pa.array(np.arange(n_rows, dtype=np.int64)),
            "k1": pa.array(rng.integers(0, N_KEYS, size=n_rows, dtype=np.int64)),
            "k2": pa.array(rng.integers(0, N_KEYS2, size=n_rows, dtype=np.int64)),
            "dim_key": pa.array(rng.integers(0, N_DIM, size=n_rows, dtype=np.int64)),
            "x": pa.array(rng.integers(-1000, 1000, size=n_rows, dtype=np.int64)),
            "price": pa.array(np.round(rng.random(n_rows, dtype=np.float64) * 1000.0, 4)),
            "qty": pa.array(rng.integers(1, 100, size=n_rows, dtype=np.int64)),
            "category": pa.array(cats, type=pa.string()),
            "ts": pa.array(ts, type=pa.timestamp("us")),
        }
    )
    dim = pa.table(
        {
            "dim_key": pa.array(np.arange(N_DIM, dtype=np.int64)),
            "region": pa.array(
                np.array(["north", "south", "east", "west"], dtype=object)[
                    rng.integers(0, 4, size=N_DIM)
                ],
                type=pa.string(),
            ),
            "weight": pa.array(np.round(rng.random(N_DIM) * 10.0, 4)),
        }
    )
    return fact, dim


@dataclass
class SyntheticContext:
    """Prepared handles for the synthetic fact/dim dataset, one per engine."""

    rows: int
    fact: pa.Table
    dim: pa.Table
    dim2: pa.Table  # first half of dim keys, for left/semi/anti joins
    con: duckdb.DuckDBPyConnection
    pf: pl.DataFrame
    pd_dim: pl.DataFrame
    pd_dim2: pl.DataFrame
    bf: bt.Dataset
    bd: bt.Dataset
    bd2: bt.Dataset
    # Lazily-built Ray Datasets, cached by name (only when the ray engine runs).
    _ray: dict[str, Any] = field(default_factory=dict, init=False, repr=False)

    def ray(self, name: str, table: pa.Table) -> Any:
        """A Ray Dataset over `table`, cached by `name`. Initializes a controlled
        local Ray (no dashboard, quiet) on first use so the benchmark is reproducible."""
        import ray
        import ray.data

        if not ray.is_initialized():
            ray.init(
                include_dashboard=False,
                ignore_reinit_error=True,
                configure_logging=False,
                log_to_driver=False,
            )
        if name not in self._ray:
            self._ray[name] = ray.data.from_arrow(table)
        return self._ray[name]

    @classmethod
    def build(cls, rows: int = DEFAULT_ROWS) -> SyntheticContext:
        fact, dim = make_synthetic_data(rows)
        # dim_key == arange(N_DIM), so the first half are exactly keys [0, N_DIM/2).
        dim2 = dim.slice(0, N_DIM // 2)
        con = duckdb.connect()
        con.register("fact", fact)
        con.register("dim", dim)
        con.register("dim2", dim2)
        return cls(
            rows=rows,
            fact=fact,
            dim=dim,
            dim2=dim2,
            con=con,
            pf=pl.from_arrow(fact),
            pd_dim=pl.from_arrow(dim),
            pd_dim2=pl.from_arrow(dim2),
            bf=bt.from_arrow(fact),
            bd=bt.from_arrow(dim),
            bd2=bt.from_arrow(dim2),
        )


# --------------------------------------------------------------------------- #
# TPC-H dataset
# --------------------------------------------------------------------------- #
DEFAULT_SF = 0.1
TPCH_TABLES = (
    "customer",
    "lineitem",
    "nation",
    "orders",
    "part",
    "partsupp",
    "region",
    "supplier",
)


def _normalize_types(table: pa.Table) -> pa.Table:
    """Cast decimals to float64 and dates to timestamp[us] for cross-engine parity."""
    arrays, fields = [], []
    for fld in table.schema:
        arr = table.column(fld.name)
        if pa.types.is_decimal(fld.type):
            arr = pc.cast(arr, pa.float64())
            fld = fld.with_type(pa.float64())
        elif pa.types.is_date(fld.type):
            arr = pc.cast(arr, pa.timestamp("us"))
            fld = fld.with_type(pa.timestamp("us"))
        arrays.append(arr)
        fields.append(fld)
    return pa.Table.from_arrays(arrays, schema=pa.schema(fields))


def make_tpch_tables(scale: float) -> dict[str, pa.Table]:
    """Generate the TPC-H tables at ``scale`` (DuckDB dbgen) as normalized Arrow."""
    con = duckdb.connect()
    con.sql("INSTALL tpch; LOAD tpch;")
    con.sql(f"CALL dbgen(sf={scale})")
    return {
        name: _normalize_types(con.sql(f"SELECT * FROM {name}").to_arrow_table())
        for name in TPCH_TABLES
    }


@dataclass
class TpchContext:
    """Prepared handles for the TPC-H tables, one mapping per engine."""

    scale: float
    tables: dict[str, pa.Table]
    con: duckdb.DuckDBPyConnection
    bt_t: dict[str, bt.Dataset]
    pl_t: dict[str, pl.DataFrame]

    @classmethod
    def build(cls, scale: float = DEFAULT_SF) -> TpchContext:
        tables = make_tpch_tables(scale)
        con = duckdb.connect()
        for name, tbl in tables.items():
            con.register(name, tbl)
        return cls(
            scale=scale,
            tables=tables,
            con=con,
            bt_t={name: bt.from_arrow(tbl) for name, tbl in tables.items()},
            pl_t={name: pl.from_arrow(tbl) for name, tbl in tables.items()},
        )
